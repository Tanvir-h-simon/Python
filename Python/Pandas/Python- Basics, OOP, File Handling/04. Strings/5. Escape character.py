# Single Quote and Backslash
text1 = 'It\'s a beautiful day! \\ Stay positive.'
print(text1)

# New Line (\n)
text2 = "Hello\nWorld!"
print(text2)

# 3. Carriage Return (\r)
text3 = "Hello\rWorld"
print(text3)   # 'World' overwrites 'Hello' partially

# Tab (\t)
text4 = "Name\tAge\tCountry"
print(text4)

# Backspace (\b)
text5 = "Python\b!"
print(text5)

# Form Feed (\f)
text6 = "Line1\fLine2"
print(text6)

# Octal value (\ooo)
text7 = "\110\145\154\154\157"  # ASCII codes for 'Hello'
print(text7)

# Hex value (\xhh)
text8 = "\x48\x65\x6C\x6C\x6F"  # Hex codes for 'Hello'
print(text8)
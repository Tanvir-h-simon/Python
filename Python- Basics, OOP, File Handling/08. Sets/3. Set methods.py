# Set constructor
# set(iterable): Converts an iterable to set.
print(set("abc"))
# word = set("abc")
# print(word)
word = set(["abc", "Hello"]) # Square brackets: Convert list to set
print(word)
fruits = set(["apple", "banana", "cherry"])
print(fruits)


fruits = {"apple", "banana", "cherry", "orange", "kiwi", "melon", "mango"}

# Set length
print(len(fruits))

# Set sum
number = {1, 2, 3, 4, 5}
print(sum(number))

# Set maximum
print(max(number))

